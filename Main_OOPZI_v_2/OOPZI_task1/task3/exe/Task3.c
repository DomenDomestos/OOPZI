//Импорт математического аппатарата для вычисления функции
#include <stdio.h>
#include <math.h>

//Импорт текстового редактора
#include <string.h>
extern double f_3(double, double);
void main(void)
{
	//Вывод, аналогичный выводу функции из task1
	printf("f=%5.4lf\n", f_3(1, 8));

	double x1, y1;
	scanf_s("%lf", &x1);
	scanf_s("%lf", &y1);
	printf("f=%5.4f\n", f_3(x1, y1));
}
double f_3(double x, double y)
{
	return (sqrt(x) - sqrt(y)) / x;
}